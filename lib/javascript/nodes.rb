module Javascript
  ArrayLiteral        = Struct.new(:elements)
  Assignment          = Struct.new(:operator, :left_hand_side, :right_hand_side)
  BinaryOperation     = Struct.new(:operator, :left_hand_side, :right_hand_side)
  Block               = Struct.new(:body, :scope)
  BooleanLiteral      = Struct.new(:value, keyword_init: true)
  Break               = Struct.new(nil)
  ConstStatement      = Struct.new(:declarations, keyword_init: true)
  Continue            = Struct.new(nil)
  DebuggerStatement   = Struct.new(nil)
  DoWhile             = Struct.new(:body, :condition)
  ExpressionStatement = Struct.new(:expression)
  FunctionCall        = Struct.new(:callee, :arguments)
  FunctionDeclaration = Struct.new(:definition)
  FunctionDefinition  = Struct.new(:name, :parameters, :body, :scope)
  Identifier          = Struct.new(:name)
  If                  = Struct.new(:condition, :consequent, :alternative)
  LetStatement        = Struct.new(:declarations, keyword_init: true)
  New                 = Struct.new(:initializer)
  NumberLiteral       = Struct.new(:value)
  NullLiteral         = Struct.new(nil)
  ObjectLiteral       = Struct.new(:properties)
  Parameter           = Struct.new(:name, :default)
  PropertyAccess      = Struct.new(:receiver, :accessor, :computed)
  PropertyDefinition  = Struct.new(:name, :value, keyword_init: true)
  Return              = Struct.new(:expression)
  Script              = Struct.new(:body, :scope)
  StatementList       = Struct.new(:statements)
  StringLiteral       = Struct.new(:value)
  Ternary             = Struct.new(:condition, :consequent, :alternative)
  Throw               = Struct.new(:expression)
  UnaryOperation      = Struct.new(:operator, :operand, :position)
  VariableDeclaration = Struct.new(:name, :value)
  VarStatement        = Struct.new(:declarations, keyword_init: true)
  While               = Struct.new(:condition, :body)
  With                = Struct.new(:expression, :body)
end
